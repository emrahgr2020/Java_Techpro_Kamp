package day03;

public class C6_IncrementDecrement {

	public static void main(String[] args) {
		int number = 10;
		
		// bir variable nin degerini toplayarak arttırmak icin 3 yontem kullanabilirim
		
		//1
		
		number = number + 5;
		
		//2.yontem
		
		number +=8;
		
		//3. yontem sadece 1 arttirmak için
		
		number++;
		
		// bu yontemler sadece toplama islemine has degildir tum operatorlerde kullanılır
		// 3. yontem sadece toplama ve cıkartma icin gecerli
		
		
		//number i 6 eksiltme
		
		number -=6;

	}

}
