package calismalar;

public class ForCalisma {

	public static void main(String[] args) {
		for (int i = 0; i <=10; i++) {
			System.out.print(i);
		}
		
		System.out.println("for dongusu bitti");
		
		int i=1;
		while (i<10) {
			System.out.println(i);
			i++;
		}
		
		System.out.println("while dongusu bitti");
		
		int j=1;
		do {
			System.out.println(j);
			j++;
		}while(j<10);

	}

}
